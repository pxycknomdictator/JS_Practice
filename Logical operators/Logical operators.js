// Logical operators in JS
/*   There are three logical operators in Js 

AND  =  &&
OR   =  ||
NOT  =   !   */

console.log('AND OPERATION &&');

let a = 67;
let b = 56;
console.log(a>b && a!=b)
console.log(a<b && a>b)
console.log(a>b && a===b)
console.log(a===b && a==b)

console.log('OR OPERATION ||');

let c = 67;
let d = 56;
console.log(c>d || c!=d)
console.log(c<d || c!=d)
console.log(c>d || c===d)
console.log(c===d || c<d)

